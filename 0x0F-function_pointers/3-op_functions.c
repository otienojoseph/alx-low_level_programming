#include "3-calc.h"

/**
 * op_add - Return sum of two ints
 * @a: param
 * @b: param
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Return difference of two ints
 * @a: param
 * @b: param
 * Return: difference of two ints
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Return product of two ints
 * @a: param
 * @b: param
 * Return: product of two ints
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Return result of division of two ints
 * @a: param
 * @b: param
 * Return: divission of two ints
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Return remainder of division of two ints
 * @a: param
 * @b: param
 * Return: remainder of division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
